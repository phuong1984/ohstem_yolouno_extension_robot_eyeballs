    def draw_eyes(self, x1 = X1_EYE_BALL_CENTER, y1 = Y0_EYE_BALL_CENTER,
                  x2 = X2_EYE_BALL_CENTER, y2 = Y0_EYE_BALL_CENTER,
                  left_pupil_dir = DIR_E, right_pupil_dir = DIR_W):
        self.oled.fill(0)
        self.buff_eye_balls(x1, y1, x2, y2)
