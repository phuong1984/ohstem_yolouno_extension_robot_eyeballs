    def scroll_image(self, index, direction="left", steps=10, delay=0.05):
        if index < 0: index = 0
        if index >= len(eyes_expressions): index = index % len(eyes_expressions)
        
        """Hiệu ứng cuộn hình ảnh."""
        if 0 <= index < len(eyes_expressions):
            self.oled.fill(0)
            fb = framebuf.FrameBuffer(eyes_expressions[index], 128, 64, framebuf.MONO_HLSB)
            
            if direction == "left":
                x = OLED_WIDTH
                self.oled.blit(fb, x, 0)  # buff image tại vị trí (128, 0)
                self.oled.show()
                while x > -OLED_WIDTH:
                    x = x - steps
                    self.oled.fill(0)
                    self.oled.blit(fb, x, 0)
                    self.oled.show()
                    time.sleep(delay)
            elif direction == "right":
                x = -OLED_WIDTH
                self.oled.blit(fb, x, 0)  # buff image tại vị trí (-128, 0)
                self.oled.show()
                while x < OLED_WIDTH:
                    x = x + steps
                    self.oled.fill(0)
                    self.oled.blit(fb, x, 0)
                    self.oled.show()
                    time.sleep(delay)
            elif direction == "up":
                y = OLED_HEIGHT
                self.oled.blit(fb, 0, y)  # buff image tại vị trí (0, 64)
                self.oled.show()                
                while y > -OLED_HEIGHT:
                    y = y - steps
                    self.oled.fill(0)
                    self.oled.blit(fb, 0, y)
                    self.oled.show()
                    time.sleep(delay)
            elif direction == "down":
                y = -OLED_HEIGHT
                self.oled.blit(fb, 0, y)  # buff image tại vị trí (0, -64)
                self.oled.show()                
                while y < OLED_HEIGHT:
                    y = y + steps
                    self.oled.fill(0)
                    self.oled.blit(fb, 0, y)
                    self.oled.show()
                    time.sleep(delay)

    
















